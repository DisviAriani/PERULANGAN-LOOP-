
package DeretAngka3;

public class DeretSegiempat_1 {
    public static void main(String[] args) {
        for (int i = 1; i <= 4; i++) {
            for (int j = 0; j <= 5; j++) {
                System.out.print(" " + (i+j));
            }
            System.out.println();
        }
    }
}
