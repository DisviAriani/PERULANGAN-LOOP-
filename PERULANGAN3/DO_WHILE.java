
package PERULANGAN3;

public class DO_WHILE {
    public static void main(String[] args) {
        int i = 10;
        do{
            System.out.println("bilangan "+i);
        }while (i-- > 0);
    }
}
//OUTPUT :
//bilangan 10
//bilangan 9
//bilangan 8
//bilangan 7
//bilangan 6
//bilangan 5
//bilangan 4
//bilangan 3
//bilangan 2
//bilangan 1
//bilangan 0